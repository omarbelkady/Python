class TakeInAndPrintString(object):
    #init means create params and __init__(self) is the def constructor
    def __init__(self):
        self.l ="AND TAYLOR SWIFT"

    @staticmethod
    def returnTheString(self):
        #How to print on the same line
        print(self,end=" ")

    def outputStringToConsoleInUpperCase(self):
        print(self.l.upper())

classObj = TakeInAndPrintString()
classObj.returnTheString("2526_56837_2")
classObj.outputStringToConsoleInUpperCase()
